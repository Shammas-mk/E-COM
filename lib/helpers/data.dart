class Product {
  final int productId;
  final String productName;
  final String strapColor;
  final String highlight;
  final int price;
  final bool status;

  const Product({
    required this.productId,
    required this.productName,
    required this.strapColor,
    required this.highlight,
    required this.price,
    required this.status,
  });
}
